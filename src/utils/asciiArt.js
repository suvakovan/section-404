// Function to format current date/time in terminal style
export const getTerminalDateTime = () => {
    const now = new Date();
    return now.toLocaleString('en-US', {
        weekday: 'short',
        year: 'numeric',
        month: 'short',
        day: 'numeric',
        hour: '2-digit',
        minute: '2-digit',
        second: '2-digit'
    });
};
